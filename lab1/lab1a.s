#include "../HC12TOOLS.INC"
	ORG $1000
VAR1 DC.W 0		;DEFINES ONE WORD INITIALIZED TO 0
VAR2 DC.W 10		;DEFINES ONE WORD INITIALIZED TO 10
VAR3 DS.W 1		;DEFINES ONE WORD UNINITIALIZED
ARRAY DS.B 100		;DEFINES 100 BYTES UNINITIALIZED

	ORG $2000
MAIN:	;THIS WILL BE THE FIRST LINE OF CODE EXECUTED
	LDX #0		;INDEX X = 0
	LDAA #1		;ACCUMULATOR A = 1
	LDY VAR2		;INDEX Y = VALUE FROM MEMORY LOCATION VAR2
LOOP1:
	STAA ARRAY,X	;STORE VALUE IN ACCUMULATOR A TO MEMORY ;LOCATION WHOSE ADDRESS IS 
;VALUE OF “ARRAY” PLUS VALUE IN X
	INX			;INCREMENT INDEX X	
	ADDA #3		;ADD 3 TO THE CONTENTS OF ACCUMULATOR A
	DBNE Y,LOOP1		;DECREMENT Y AND BRANCH TO LOOP1 IF RESULT 
	;IS NOT EQUAL TO ZERO
	MOVW #$ABCD,VAR3 ;PLACE HEXADECIMAL VALUE ABCD IN MEMORY
;AT LOCATION VAR3
	RTS			;END OF “MAIN” FUNCTION
